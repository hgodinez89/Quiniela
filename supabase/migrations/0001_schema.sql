create table if not exists public.stadiums (
  id          serial primary key,
  name        text not null,
  city        text not null,
  country     text not null
);

create table if not exists public.teams (
  id            serial primary key,
  code          text not null unique,          -- p. ej. 'MEX'
  name          text not null,
  flag_emoji    text,
  group_letter  char(1)                          -- A..L; null si solo placeholder
);

create table if not exists public.matches (
  id              bigint generated by default as identity primary key,
  external_id     text unique,                   -- id de la fuente de marcadores
  stage           match_stage not null,
  group_letter    char(1),                       -- solo fase de grupos
  matchday        int,                           -- jornada (orden dentro de fase)
  home_team_id    int references public.teams(id),
  away_team_id    int references public.teams(id),
  home_placeholder text,                          -- 'Ganador Grupo A', etc. (knockout)
  away_placeholder text,
  stadium_id      int references public.stadiums(id),
  kickoff_at      timestamptz not null,
  status          match_status not null default 'scheduled',
  home_score      int check (home_score >= 0),
  away_score      int check (away_score >= 0),
  updated_at      timestamptz not null default now()
);
create index if not exists matches_kickoff_idx on public.matches (kickoff_at);
create index if not exists matches_stage_idx on public.matches (stage);

-- ----------------------------------------------------------------------------
