-- ============================================================================
-- Funciones helper (SECURITY DEFINER para evitar recursión de RLS)
-- ============================================================================
create or replace function public.my_email()
returns text language sql stable security definer set search_path = public, pg_temp as $$
  select lower(coalesce(auth.jwt() ->> 'email', ''));
$$;

create or replace function public.is_member(p_group uuid)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1 from public.group_members
    where group_id = p_group and user_id = auth.uid()
  );
$$;

create or replace function public.is_creator(p_group uuid)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1 from public.bet_groups
    where id = p_group and creator_id = auth.uid()
  );
$$;

create or replace function public.shares_group(p_other uuid)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1
    from public.group_members a
    join public.group_members b on a.group_id = b.group_id
    where a.user_id = auth.uid() and b.user_id = p_other
  );
$$;

create or replace function public.has_pending_invite(p_group uuid)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1 from public.group_invitations
    where group_id = p_group and invited_email = public.my_email() and status = 'pending'
  );
$$;

-- ¿El viewer puede ver predicciones ajenas de este partido en este grupo?
-- Solo si ya envió la fase del partido en ese grupo.
create or replace function public.can_see_prediction(p_group uuid, p_match bigint)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1
    from public.matches m
    join public.phase_submissions ps
      on ps.group_id = p_group and ps.user_id = auth.uid() and ps.stage = m.stage
    where m.id = p_match
  );
$$;

-- ¿Se puede crear/editar una predicción? Solo si el partido no ha iniciado
-- y la fase no ha sido enviada por el usuario en ese grupo.
create or replace function public.can_edit_prediction(p_group uuid, p_match bigint)
returns boolean language sql stable security definer set search_path = public, pg_temp as $$
  select exists (
    select 1
    from public.matches m
    where m.id = p_match
      and m.kickoff_at > now()
      and not exists (
        select 1 from public.phase_submissions ps
        where ps.group_id = p_group and ps.user_id = auth.uid() and ps.stage = m.stage
      )
  );
$$;

-- ============================================================================
-- Trigger: crear profile al registrarse
-- ============================================================================
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = public, pg_temp as $$
begin
  insert into public.profiles (id, display_name, avatar_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name', split_part(new.email, '@', 1)),
    new.raw_user_meta_data ->> 'avatar_url'
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- Trigger: mantener updated_at
create or replace function public.touch_updated_at()
returns trigger language plpgsql as $$
begin new.updated_at = now(); return new; end; $$;

drop trigger if exists predictions_touch on public.predictions;
create trigger predictions_touch before update on public.predictions
  for each row execute function public.touch_updated_at();

drop trigger if exists matches_touch on public.matches;
create trigger matches_touch before update on public.matches
  for each row execute function public.touch_updated_at();

-- ============================================================================
-- Vistas de cálculo (volumen pequeño; sin triggers de puntaje)
-- ============================================================================
-- Puntos por predicción: exacto = 3, acierta resultado = 1, else 0
create or replace view public.v_prediction_points as
  select
    p.id        as prediction_id,
    p.group_id,
    p.user_id,
    p.match_id,
    m.stage,
    case
      when m.status = 'finished'
           and p.home_score = m.home_score and p.away_score = m.away_score then 3
      when m.status = 'finished'
           and sign(p.home_score - p.away_score) = sign(m.home_score - m.away_score) then 1
      else 0
    end as points
  from public.predictions p
  join public.matches m on m.id = p.match_id;

-- Ranking por grupo (suma de puntos + posición)
create or replace view public.v_group_ranking as
  with totals as (
    select gm.group_id, gm.user_id, coalesce(sum(vp.points), 0) as total_points
    from public.group_members gm
    left join public.v_prediction_points vp
      on vp.group_id = gm.group_id and vp.user_id = gm.user_id
    group by gm.group_id, gm.user_id
  )
  select
    group_id,
    user_id,
    total_points,
    rank() over (partition by group_id order by total_points desc) as position
  from totals;

-- Las vistas se ejecutan con permisos del invocador (security_invoker) para
-- respetar las RLS de las tablas base.
alter view public.v_prediction_points set (security_invoker = on);
alter view public.v_group_ranking set (security_invoker = on);

