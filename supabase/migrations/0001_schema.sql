-- Catálogo: lectura para autenticados; escritura solo service_role (sin política)
create policy "stadiums readable" on public.stadiums for select to authenticated using (true);
create policy "teams readable"    on public.teams    for select to authenticated using (true);
create policy "matches readable"  on public.matches  for select to authenticated using (true);

-- profiles
create policy "profiles select own or shared" on public.profiles
  for select to authenticated
  using (id = auth.uid() or public.shares_group(id));
create policy "profiles insert own" on public.profiles
  for insert to authenticated with check (id = auth.uid());
create policy "profiles update own" on public.profiles
  for update to authenticated using (id = auth.uid()) with check (id = auth.uid());

-- bet_groups
create policy "groups select member or creator" on public.bet_groups
  for select to authenticated
  using (creator_id = auth.uid() or public.is_member(id));
create policy "groups insert as creator" on public.bet_groups
  for insert to authenticated with check (creator_id = auth.uid());
create policy "groups update by creator" on public.bet_groups
  for update to authenticated using (creator_id = auth.uid()) with check (creator_id = auth.uid());
create policy "groups delete by creator" on public.bet_groups
  for delete to authenticated using (creator_id = auth.uid());

-- group_members
create policy "members select within group" on public.group_members
  for select to authenticated
  using (user_id = auth.uid() or public.is_member(group_id));
-- Alta: el creador se agrega a sí mismo (admin) o un invitado acepta su invitación
create policy "members insert self if creator or invited" on public.group_members
  for insert to authenticated
  with check (
    user_id = auth.uid()
    and (public.is_creator(group_id) or public.has_pending_invite(group_id))
  );
-- Baja: solo el creador puede eliminar miembros (y no a sí mismo)
create policy "members delete by creator" on public.group_members
  for delete to authenticated
  using (public.is_creator(group_id) and user_id <> auth.uid());

-- group_invitations
create policy "invites select creator or invitee" on public.group_invitations
  for select to authenticated
  using (public.is_creator(group_id) or invited_email = public.my_email());
create policy "invites insert by creator only" on public.group_invitations
  for insert to authenticated
  with check (public.is_creator(group_id) and invited_by = auth.uid());
create policy "invites update invitee or creator" on public.group_invitations
  for update to authenticated
  using (invited_email = public.my_email() or public.is_creator(group_id))
  with check (invited_email = public.my_email() or public.is_creator(group_id));
create policy "invites delete by creator" on public.group_invitations
  for delete to authenticated using (public.is_creator(group_id));

-- predictions
create policy "predictions select own or visible" on public.predictions
  for select to authenticated
  using (
    user_id = auth.uid()
    or (public.is_member(group_id) and public.can_see_prediction(group_id, match_id))
  );
create policy "predictions insert own editable" on public.predictions
  for insert to authenticated
  with check (
    user_id = auth.uid()
    and public.is_member(group_id)
    and public.can_edit_prediction(group_id, match_id)
  );
create policy "predictions update own editable" on public.predictions
  for update to authenticated
  using (user_id = auth.uid() and public.can_edit_prediction(group_id, match_id))
  with check (user_id = auth.uid() and public.can_edit_prediction(group_id, match_id));
create policy "predictions delete own editable" on public.predictions
  for delete to authenticated
  using (user_id = auth.uid() and public.can_edit_prediction(group_id, match_id));

-- phase_submissions
create policy "submissions select within group" on public.phase_submissions
  for select to authenticated
  using (user_id = auth.uid() or public.is_member(group_id));
create policy "submissions insert own" on public.phase_submissions
  for insert to authenticated
  with check (user_id = auth.uid() and public.is_member(group_id));

