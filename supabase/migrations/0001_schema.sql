-- ----------------------------------------------------------------------------
-- Grupos de apuestas
-- ----------------------------------------------------------------------------
create table if not exists public.bet_groups (
  id          uuid primary key default gen_random_uuid(),
  name        text not null check (char_length(name) between 1 and 80),
  description text check (char_length(description) <= 500),
  creator_id  uuid not null references auth.users(id) on delete cascade,
  created_at  timestamptz not null default now()
);
create index if not exists bet_groups_creator_idx on public.bet_groups (creator_id);

create table if not exists public.group_members (
  id        uuid primary key default gen_random_uuid(),
  group_id  uuid not null references public.bet_groups(id) on delete cascade,
  user_id   uuid not null references auth.users(id) on delete cascade,
  role      member_role not null default 'member',
  joined_at timestamptz not null default now(),
  unique (group_id, user_id)
);
create index if not exists group_members_user_idx on public.group_members (user_id);
create index if not exists group_members_group_idx on public.group_members (group_id);

create table if not exists public.group_invitations (
  id            uuid primary key default gen_random_uuid(),
  group_id      uuid not null references public.bet_groups(id) on delete cascade,
  invited_email text not null,                  -- siempre en minúsculas, dominio gmail
  invited_by    uuid not null references auth.users(id) on delete cascade,
  status        invitation_status not null default 'pending',
  created_at    timestamptz not null default now(),
  unique (group_id, invited_email)
);
create index if not exists group_invitations_email_idx on public.group_invitations (invited_email);

-- ----------------------------------------------------------------------------
-- Predicciones (por grupo, por usuario, por partido)
-- ----------------------------------------------------------------------------
create table if not exists public.predictions (
  id         uuid primary key default gen_random_uuid(),
  group_id   uuid not null references public.bet_groups(id) on delete cascade,
  user_id    uuid not null references auth.users(id) on delete cascade,
  match_id   bigint not null references public.matches(id) on delete cascade,
  home_score int not null check (home_score between 0 and 30),
  away_score int not null check (away_score between 0 and 30),
  updated_at timestamptz not null default now(),
  unique (group_id, user_id, match_id)
);
create index if not exists predictions_group_user_idx on public.predictions (group_id, user_id);

-- Envío/bloqueo de una fase por usuario y grupo (gobierna visibilidad de ajenas)
create table if not exists public.phase_submissions (
  id           uuid primary key default gen_random_uuid(),
  group_id     uuid not null references public.bet_groups(id) on delete cascade,
  user_id      uuid not null references auth.users(id) on delete cascade,
  stage        match_stage not null,
  submitted_at timestamptz not null default now(),
  unique (group_id, user_id, stage)
);

